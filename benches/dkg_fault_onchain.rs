fn env_u64(name: &str) -> Option<u64> {
    let value = env::var(name).ok()?;
    Some(
        value
            .parse()
            .unwrap_or_else(|err| panic!("{name} must be an unsigned integer: {err}")),
    )
}

fn assert_all_within_tx_budget(reports: &[BudgetReport], limits: ExUnits) {
    let violations = reports
        .iter()
        .filter(|report| report.budget.mem > limits.mem || report.budget.cpu > limits.cpu)
        .map(|report| {
            format!(
                "{}: mem={} cpu={} limits_mem={} limits_cpu={}",
                report.name, report.budget.mem, report.budget.cpu, limits.mem, limits.cpu
            )
        })
        .collect::<Vec<_>>();

    assert!(
        violations.is_empty(),
        "on-chain DKG fault benchmarks exceed transaction ExUnit limits:\n{}",
        violations.join("\n")
    );
}

