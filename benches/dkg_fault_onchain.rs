fn setup(params: AxiomDkgCircuitParams) -> ParamsKZG<Bls12> {
    ParamsKZG::<Bls12>::setup(params.degree, StdRng::seed_from_u64(2))
}

fn keygen(
    setup: &ParamsKZG<Bls12>,
    keygen_builder: halo2_base::gates::circuit::builder::RangeCircuitBuilder<BlsFr>,
) -> (ProvingKey<G1Affine>, Vec<Vec<usize>>, KeygenTimes) {
    let vk_start = Instant::now();
    let vk = keygen_vk(setup, &keygen_builder).expect("vkey generation should succeed");
    let vk_time = vk_start.elapsed();

    let pk_start = Instant::now();
    let pk = keygen_pk(setup, vk, &keygen_builder).expect("pkey generation should succeed");
    let pk_time = pk_start.elapsed();

    let break_points = keygen_builder.break_points();
    (pk, break_points, KeygenTimes { vk_time, pk_time })
}

fn prove_round1_digest_fault_case(
    setup: &ParamsKZG<Bls12>,
    pk: &ProvingKey<G1Affine>,
    config_params: halo2_base::gates::circuit::BaseCircuitParams,
    break_points: Vec<Vec<usize>>,
    params: AxiomDkgCircuitParams,
    case: &Round1Case,
) -> DigestProofArtifact {
    let witness_start = Instant::now();
    let (prover_builder, public_instances) = build_round1_digest_fault_prover_circuit(
        config_params,
        break_points,
        params,
        &case.witness,
    );
    let witness_time = witness_start.elapsed();

    let expected_digest = round1_message_digest(params, &case.witness);
    assert_eq!(public_instances, vec![expected_digest]);
    assert_eq!(case.witness.challenge, round1_hdk_challenge(&case.witness));
    let computed = round1_digest_residual(&case.witness);
    assert_eq!(
        computed == case.witness.transcript_r,
        case.expect_residual_matches_transcript
    );
    assert!(!case.expect_residual_matches_transcript);

    prove_digest_artifact(
        setup,
        pk,
        prover_builder,
        public_instances,
        expected_digest,
        case.name.clone(),
        witness_time,
        "public input = signed Poseidon(message), circuit derives HDKG and asserts D != R",
    )
}

