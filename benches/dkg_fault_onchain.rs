fn prove_digest_artifact<C>(
    setup: &ParamsKZG<Bls12>,
    pk: &ProvingKey<G1Affine>,
    circuit: C,
    public_instances: Vec<BlsFr>,
    digest: BlsFr,
    name: String,
    witness_time: Duration,
    host_check: &'static str,
) -> DigestProofArtifact
where
    C: Circuit<BlsFr>,
{
    let proof_start = Instant::now();
    let proof = gen_proof(setup, pk, circuit, &public_instances);
    let proof_time = proof_start.elapsed();

    let verify_start = Instant::now();
    verify(setup, pk, &proof, &public_instances);
    let verify_time = verify_start.elapsed();

    let (spo_vkey, spo_sig) = sign_digest(digest);
    DigestProofArtifact {
        name,
        proof,
        public_instances,
        digest,
        spo_vkey,
        spo_sig,
        host_check,
        witness_time,
        proof_time,
        verify_time,
    }
}

fn gen_proof<C>(
    params: &ParamsKZG<Bls12>,
    pk: &ProvingKey<G1Affine>,
    circuit: C,
    public_instances: &[BlsFr],
) -> Vec<u8>
where
    C: Circuit<BlsFr>,
{
    let rng = StdRng::seed_from_u64(1);
    let instances: &[&[BlsFr]] = &[public_instances];
    let mut transcript = CardanoBlake2bWrite::<_, G1Affine>::init(vec![]);
    create_proof::<
        KZGCommitmentScheme<Bls12>,
        ProverSHPLONK<'_, Bls12>,
        Challenge255<_>,
        _,
        CardanoBlake2bWrite<Vec<u8>, G1Affine>,
        _,
    >(params, pk, &[circuit], &[instances], rng, &mut transcript)
    .expect("proof generation should succeed");
    transcript.finalize()
}

fn verify(
    params: &ParamsKZG<Bls12>,
    pk: &ProvingKey<G1Affine>,
    proof: &[u8],
    public_instances: &[BlsFr],
) {
    let verifier_params = params.verifier_params();
    let strategy = SingleStrategy::new(params);
    let instances: &[&[BlsFr]] = &[public_instances];
    let mut transcript = CardanoBlake2bRead::<_, G1Affine>::init(proof);
    verify_proof::<
        KZGCommitmentScheme<Bls12>,
        VerifierSHPLONK<'_, Bls12>,
        Challenge255<G1Affine>,
        CardanoBlake2bRead<&[u8], G1Affine>,
        SingleStrategy<'_, Bls12>,
    >(
        verifier_params,
        pk.get_vk(),
        strategy,
        &[instances],
        &mut transcript,
    )
    .expect("proof verification should succeed");
}

fn generate_digest_project(
    round_name: &str,
    setup: &ParamsKZG<Bls12>,
    pk: &ProvingKey<G1Affine>,
    corrupted: &DigestProofArtifact,
    honest: &DigestProofArtifact,
) -> PathBuf {
    let project_dir = prepare_project(round_name);
    let paths = generator_paths(&project_dir);

    let generator_start = Instant::now();
    generate_axiom_shplonk_aiken_from_vk_and_instances(
        setup,
        pk.get_vk(),
        &corrupted.proof,
        &[corrupted.public_instances.as_slice()],
        &paths,
    )
    .expect("Axiom SHPLONK verifier generation should succeed");
    println!(
        "{round_name}_generic_generator_time: {}",
        fmt_duration(generator_start.elapsed())
    );

    write_digest_policy(&project_dir, round_name, corrupted, honest);
    project_dir
}

